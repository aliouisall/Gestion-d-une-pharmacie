    public void selectionall(){
        try {
            myconObj = DriverManager.getConnection("jdbc:mysql://localhost:3306/Gestion_Pharmacie", "root", "");
            mystatObj = myconObj.createStatement();
            myresObj = mystatObj.executeQuery("SELECT * FROM Produit");
            Produit.setModel(DbUtils.resultSetToTableModel(myresObj));
        }
        catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
    public void ajout() {
        try {
            PreparedStatement st = myconObj.prepareStatement("INSERT INTO Produit VALUES (?, ?, ?, ?)");
            st.setString(1, TxtFieldIdProd.getText());
            st.setString(2, TxtFieldNomProd.getText());
            st.setString(3, TxtFieldPrixProd.getText());
            st.setString(4, TxtFieldTypeProd.getText());
            st.executeUpdate();
            TxtFieldIdProd.setText("");
            TxtFieldNomProd.setText("");
            TxtFieldPrixProd.setText("");
            TxtFieldTypeProd.setText("");
        }
        catch (SQLException E) {
            E.printStackTrace();
        }
        selectionall();
    }

